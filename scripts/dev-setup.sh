cleanup_on_failure() {
    log_error "Setup failed. Cleaning up..."
    docker compose -f "$DEV_COMPOSE_FILE" down --remove-orphans
    exit 1
}

# Main setup flow
main() {
    print_banner
    
    # Set up error handling
    trap cleanup_on_failure ERR
    
    log_info "Starting development environment setup..."
    
    check_requirements
    setup_environment
    setup_directories
    build_development_images
    start_development_stack
    seed_test_data
    
    if verify_services; then
        show_development_info
        log_success "🎉 Development environment setup completed successfully!"
        
        # Optional: Open browser to main application
        if command -v open &> /dev/null; then
            log_info "Opening application in browser..."
            open http://localhost:3000
        elif command -v xdg-open &> /dev/null; then
            log_info "Opening application in browser..."
            xdg-open http://localhost:3000
        fi
        
    else
        log_error "Some services failed verification. Check logs and try restarting individual services."
        exit 1
    fi
}

# Run with error handling
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    main "$@"
fi
